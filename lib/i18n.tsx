const CODES = new Set<string>(LANGS.map((l) => l.code));

type I18n = { lang: Locale; setLang: (l: Locale) => void; t: (k: Keys) => string };
const Ctx = createContext<I18n>({ lang: "en", setLang: () => {}, t: (k) => EN[k] });

export function LangProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Locale>("en");

  useEffect(() => {
    try {
      const stored = localStorage.getItem("ig-lang");
      if (stored && CODES.has(stored)) {
        setLangState(stored as Locale);
      } else {
        const nav = navigator.language?.slice(0, 2).toLowerCase();
        if (nav && nav !== "en" && CODES.has(nav)) setLangState(nav as Locale);
      }
    } catch { /* default en */ }
  }, []);

  function setLang(l: Locale) {
    setLangState(l);
    try { localStorage.setItem("ig-lang", l); } catch {}
  }

  useEffect(() => { document.documentElement.lang = lang; }, [lang]);

  const t = (k: Keys) => STRINGS[lang]?.[k] ?? EN[k];

  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export function useI18n(): I18n {
  return useContext(Ctx);
}
